// 709. To Lower Case

// Given a string s, return the string after replacing every uppercase letter with the same lowercase letter.

// Example 1:

// Input: s = "Hello"
// Output: "hello"
// Example 2:

// Input: s = "here"
// Output: "here"
// Example 3:

// Input: s = "LOVELY"
// Output: "lovely"

/**
 * @param {string} s
 * @return {string}
 */
var toLowerCase = function(s) {  // Non Parameters
    return s.toLowerCase()   // The string converted to lowercase.
};
